from docx import Document

# Загружаем существующий документ
doc_path = r"C:\Users\rosti\OneDrive\Рабочий стол\лекции философия.docx"

try:
    # Пытаемся открыть существующий документ
    doc = Document(doc_path)
except:
    # Если файл не существует, создаем новый
    doc = Document()
    print("Создан новый документ, так как указанный файл не найден")

# Добавляем новую лекцию
lecture_title = "1. ФА как раздел философии\n2. Круг проблем современной ФА"
doc.add_page_break()
doc.add_heading(lecture_title, level=1)

content = """
## 1. Антропология
- **Определение**: "слово о человеке" - совокупность научных дисциплин, занимающихся изучением человека, его происхождения, развитие и существование в естественных и искусственных средах.
- **Специализация**: Каждая из этих наук рассматривает отдельные стороны человека, например:
  - психологическая антропология рас,
  - субъективный мир человека,
  - педагогическая А участника воспитательного процесса.
- **В СССР**: господствовала физическая А - наука о происхождении и эволюции человека и его рас.

### Философская антропология (ФА)
- **Цель**: стремится создать целостный образ человека в единстве его качеств (биологическое, общественное, духовное).
- **Значение**: один из фундаментальных разделов философского знания.
- **Кант**: "Вопросы философии в итоге сводятся к одному: что такое человек?"

#### Три основных значения термина ФА:
1. **В широком смысле** - философское знание, изучающее человека как особый род бытия.
2. **В узком смысле** - ФА - направление философии 20 века, основоположник - немецкий М. Шелер (1874-1928), который призывал объяснять природу человека и мира из человека и через человека.
   - Мир теряет качество монолитности и устойчивости, а человек становится проблематичным.
   - Человек больше не знает, кто и что он, одновременно знал о своём поведении.
3. **Стиль мышления** - особый стиль метод мышления, который использует самостоятельные нестандартные средства, отличные от языка объяснения классической философии и науки.

#### Предметное поле ФА включает:
1. Каковы формы и способы существования человека?
2. Каким образом человек реализуется в мире? Что он несёт миру своей творческой деятельностью?
3. Какова природа человека?

## Основные подходы к проблеме человека в ФА:

### 1. Дескриптивный
Концентрирует внимание на описании признаков, отличающих человека от других видов.

**Характерные признаки человека:**
- Уникальность человека во вселенной
- Умение мыслить, осуществлять свободный выбор
- Выносить моральное суждение, брать на себя ответственность

### 2. Атрибутивный
Поиск главенствующего признака, отделяющего человека от животных (например, "разумность").

### 3. Сущностное определение человека
Попытка создания концепции, согласующейся с эмпирическими данными и перспективами развития.

**Направления:**
1. **Натуралистическая концепция**:
   - Специфика человека – в особой телесности и высокой биологической организованности.
2. **Религиозная концепция**:
   - Человек есть образ и подобие Бога.
3. **Рационалистическая концепция**:
   - Сила человека - в его разуме, дающем абсолютное знание.
4. **Деятельностно-социальная концепция (К. Маркс)**:
   - Природа человека - социобиологическое образование, где социальное преобразует биологическое.
5. **Экзистенциальная концепция**:
   - Акцент на уникальности человека как неопределившегося существа, создающего свою сущность в процессе существования.

### Итоговое понимание человека в современной философии:
- **Уникален** (связан с космосом) и **универсален** (соотношение внутреннего и внешнего).
- **Социальное существо**, единство биологического, рационального и чувственного.
- **Историческое существо**, несущее ответственность перед собой и миром.

### Современные методы ФА:
1. Философская герменевтика (изучение текстов)
2. Психоаналитическая антропология
3. Социальная антропология
"""

for paragraph in content.strip().split("\n"):
    if paragraph.startswith("## "):
        doc.add_heading(paragraph[3:], level=2)
    elif paragraph.startswith("### "):
        doc.add_heading(paragraph[4:], level=3)
    elif paragraph.startswith("#### "):
        doc.add_heading(paragraph[5:], level=4)
    elif paragraph.startswith("- "):
        doc.add_paragraph(paragraph, style="List Bullet")
    elif (
        paragraph.startswith("1. ")
        or paragraph.startswith("2. ")
        or paragraph.startswith("3. ")
        or paragraph.startswith("4. ")
        or paragraph.startswith("5. ")
    ):
        doc.add_paragraph(paragraph, style="List Number")
    else:
        doc.add_paragraph(paragraph)

# Сохраняем обновлённый файл
doc.save(doc_path)
doc_path
